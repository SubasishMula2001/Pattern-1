import java.util.Scanner;
public class SquarePatten{
public static void main(String []args){
  Scanner s=new Scanner(System.in);
    int i,j,n;
    n=s.nextInt();
    for(i=1;i<=n;i++){
        
        for(j=1;j<=n;j++){
            System.out.print(n);
         
        }
        System.out.println();
        
    }
}
}